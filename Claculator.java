public class Calculator {

    public static int add (int a, int b){
        int c = a + b;
        return c;
    }

    public static int sub(int a, int b){
        int c = a-b;
        return c;
    }

    public static int mul(int a, int b){
        int c = a *b;
        return c;
    }
    public static double div (int a, int b){
        double c = a/b;
        return c;
    }
}

